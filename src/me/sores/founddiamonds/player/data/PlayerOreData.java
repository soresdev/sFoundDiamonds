package me.sores.founddiamonds.player.data;

/**
 * Created by sores on 1/24/2019.
 */
public class PlayerOreData {

    private int diamonds, gold, iron, redstone, coal, stone, emerald, lapis;

    public PlayerOreData() {
        diamonds = 0;
        gold = 0;
        iron = 0;
        redstone = 0;
        coal = 0;
        stone = 0;
        emerald = 0;
        lapis = 0;
    }

    public int getDiamonds() {
        return diamonds;
    }

    public void setDiamonds(int diamonds) {
        this.diamonds = diamonds;
    }

    public int getGold() {
        return gold;
    }

    public void setGold(int gold) {
        this.gold = gold;
    }

    public int getIron() {
        return iron;
    }

    public void setIron(int iron) {
        this.iron = iron;
    }

    public int getRedstone() {
        return redstone;
    }

    public void setRedstone(int redstone) {
        this.redstone = redstone;
    }

    public int getCoal() {
        return coal;
    }

    public void setCoal(int coal) {
        this.coal = coal;
    }

    public int getStone() {
        return stone;
    }

    public void setStone(int stone) {
        this.stone = stone;
    }

    public int getEmerald() {
        return emerald;
    }

    public void setEmerald(int emerald) {
        this.emerald = emerald;
    }

    public int getLapis() {
        return lapis;
    }

    public void setLapis(int lapis) {
        this.lapis = lapis;
    }
}
